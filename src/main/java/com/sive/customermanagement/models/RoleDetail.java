package com.sive.customermanagement.models;

class RoleDetail {
    private String jointRelationDeliveryOption;
    private int jointRelationRoleId;
    private String jointRelationRoleNote;

    // Getters and Setters
    public String getJointRelationDeliveryOption() { return jointRelationDeliveryOption; }
    public void setJointRelationDeliveryOption(String jointRelationDeliveryOption) { this.jointRelationDeliveryOption = jointRelationDeliveryOption; }
    public int getJointRelationRoleId() { return jointRelationRoleId; }
    public void setJointRelationRoleId(int jointRelationRoleId) { this.jointRelationRoleId = jointRelationRoleId; }
    public String getJointRelationRoleNote() { return jointRelationRoleNote; }
    public void setJointRelationRoleNote(String jointRelationRoleNote) { this.jointRelationRoleNote = jointRelationRoleNote; }
}