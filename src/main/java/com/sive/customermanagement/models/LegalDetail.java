package com.sive.customermanagement.models;

class LegalDetail {
    private String legalId;
    private String legalDocumentName;
    private String legalHolderName;
    private String legalIssueAuthorisedDate;
    private String legalIssueCountry;
    private String legalIssueDate;
    private String legalExpiredDate;

    // Getters and Setters
    public String getLegalId() { return legalId; }
    public void setLegalId(String legalId) { this.legalId = legalId; }
    public String getLegalDocumentName() { return legalDocumentName; }
    public void setLegalDocumentName(String legalDocumentName) { this.legalDocumentName = legalDocumentName; }
    public String getLegalHolderName() { return legalHolderName; }
    public void setLegalHolderName(String legalHolderName) { this.legalHolderName = legalHolderName; }
    public String getLegalIssueAuthorisedDate() { return legalIssueAuthorisedDate; }
    public void setLegalIssueAuthorisedDate(String legalIssueAuthorisedDate) { this.legalIssueAuthorisedDate = legalIssueAuthorisedDate; }
    public String getLegalIssueCountry() { return legalIssueCountry; }
    public void setLegalIssueCountry(String legalIssueCountry) { this.legalIssueCountry = legalIssueCountry; }
    public String getLegalIssueDate() { return legalIssueDate; }
    public void setLegalIssueDate(String legalIssueDate) { this.legalIssueDate = legalIssueDate; }
    public String getLegalExpiredDate() { return legalExpiredDate; }
    public void setLegalExpiredDate(String legalExpiredDate) { this.legalExpiredDate = legalExpiredDate; }
}